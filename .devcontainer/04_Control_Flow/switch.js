// switch in javascript is a statement that executes different code blocks based on different conditions
 
const month = 3

switch (month) {
    case 1:
        console.log(`This month is January.`)
        break;
    case 2:
        console.log(`This month is February.`)
        break;
    case 3:
        console.log(`This month is March.`)
        break;
    case 4:
        console.log(`This month is April.`)
        break;
    
    default:
        console.log(`Default month`)
        break;
}

